#include "../libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*end_node;

	if (lst == 0 || new == 0)
		return ;
	if (*lst == 0)
	{
		*lst = new;
		new->next = 0;
		return ;
	}
	end_node = ft_lstlast(*lst);
	end_node->next = *(&(new));
}
