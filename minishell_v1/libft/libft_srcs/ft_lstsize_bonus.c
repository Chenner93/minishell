#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*current_struct;
	int		i;

	current_struct = lst;
	i = 0;
	while (current_struct != 0)
	{
		i++;
		current_struct = current_struct->next;
	}
	return (i);
}
